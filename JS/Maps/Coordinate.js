class Coordinate {
  
        constructor(x, y)
        {
            this.x = x;
            this.y = y;
        }
        
        getXCoordinate() {
            return this.x;
        }
        
        getYCoordinate() {
            return this.y;
        }
        
        setXCoordinate(x) {
            this.x = x;
        }
        
        setYCoordinate(y) {
            this.y = y;
        }
}